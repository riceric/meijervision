<h1>Vision Center at Meijer Refund Policy</h1>
<p><em>Effective July 2013</em></p>

<h2>Complimentary Adjustments</h2>

<p>The Vision Center at Meijer will re-adjust eyeglasses at no charge for as long as the patient owns them.  For glasses we do not carry, or high-end brands, patients must sign a form acknowledging that we can&rsquo;t be held responsible for breakage during a fitting.</p>

<h2>Manufacturer&rsquo;s Warranty</h2>

<p>If a patient&rsquo;s frame or lenses accidentally break due to a manufacturer&rsquo;s defect, within one year of the date the patient received their glasses, the Vision Center at Meijer will repair or replace them.  The 
patient is required to return the parts and glasses for the warranty to be in effect.  If the same frame is not available, we&rsquo;ll replace it with one of comparable value, at our option.  Any product returned for replacement under this warranty, will be covered for the balance of the warranty, from the original date of purchase.</p>

<p>We will not replace a patient&rsquo;s frame or lenses that were damaged due to patient abuse or misuse.  
If there is any question regarding how the frames were damaged, please contact your area 
management for assistance making the decision.</p>

<p><strong>In case of defects in the AR coating:</strong><br />  
The Vision Center at Meijer will supply one lens replacement for one year following purchase, 
provided that the new lenses must be exact duplicates of the original order.</p> 

<h2>Prescription Change</h2>

<p>If within 30 days of date of purchase the patient&rsquo;s prescription changes we will, upon presentation of a new eyeglass prescription, remake the lenses once in the same style, lens type, and color.</p>

<h2>Refund Policy</h2> 

<p>Purchases can be returned within 30 days. The frame will be refunded in full. The lenses will be refunded at 50% of purchase price (lenses have been customized for your prescription and can not be resold).</p> 
<ul>
<li>There will be no refund on opened boxes of contacts lenses.</li>
<li>There will be no refunds on services/copays rendered.</li>
<li>All sunglasses and accessories must be in the original packaging for returns.</li>
</ul>

