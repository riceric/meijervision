<h2>Dr. Adam Black, O.D.</h2>
<p>Dr. Adam Black, O.D., has a Bachelor’s degree in Microbiology from the University of Illinois at Urbana-Champaign, a Master’s in Business Administration from the University of Colorado at Denver, and a Doctorate of Optometry from the University of California at Berkeley.  After completion of his higher education, he returned to practice optometry in Illinois to be closer to his family.</p>
<p>Throughout his career, he identified a need to simplify the administrative processes for doctors in order to allow them to focus on what really matters: providing patients with exceptional care rather than become bogged down with paperwork.  With this goal in mind, he created Black Eye Group, L.L.C. to serve all doctors working within the Meijer Vision Centers in the state of Illinois.</p>
<p>Dr. Black regularly performs eye exams at the Algonquin, Bolingbrook, Elgin, and Oswego locations.</p>

<h2>Dr. Frederick Kahle, O.D.</p>
<p>Dr. Frederick Kahle, O.D., went to undergraduate college at Western Illinois University where he majored in Biology Education.  After graduation, he attended the Illinois College of Optometry in Chicago where he received his professional degree.  
<p>He decided to become licensed to practice optometry in both Illinois and Texas, but being raised in the Crystal Lake, he ultimately decided to remain in the area.  He worked at Service Optical in Crystal Lake for over 14 years until the store closed in 2004.  He is looking forward to reconnecting with the many patients he saw during that time.
<p>Dr. Kahle performs eye exams at the Algonquin and Elgin locations.</p>
 

<h2>Dr. Lyudmila Podberezin, O.D. – “Dr. Lyuda”</h2>
<p>Dr. Lyudmila Podberezin, O.D., is proud to be Alumni of the Illinois College of Optometry and a member of the American and Illinois Optometric Associations.  She had ophthalmology training in Moscow before moving to the United States, so she has unique experience in both optometry and ophthalmology.  She is also fluent in Russian.</p>
<p>Her skills have made it easy to fit patients for bifocal and astigmatism correction contacts, as well as treating eye infections, dry eye, and allergies.  In practicing optometry, she combines outstanding skills in clinical management with caring interpersonal interaction and communication.  These attributes make her a valuable member of Black Eye Group, L.L.C., where she carries on its tradition of treating each patient as a member of the family.</p>
<p>Being a long time resident of the Northwest Chicago suburbs, Dr. Podberezin provides premier optometric care to the community that she considers home.  She and her husband Mark are proud parents of their son Alex.  In her free time, she enjoys travel and spending time with her family.</p>
<p>Dr. Podberezin performs eye exams at the Algonquin and McHenry locations.</p>

<h2>Dr. Cameron Lapp, O.D.</h2>
<p>Dr. Cameron Lapp, O.D., graduated from the Illinois College of Optometry in May of 2000.  He specialized in visual therapy during his first three years of practice.  He has extensive experience in delivering eye care to both retirement centers and industrial factories throughout the Chicagoland area.  He is fluent in the fitting of all types of contact lenses, and he diagnoses and manages medical conditions that affect the eye.  We are proud to have him as a member of our team of doctors.</p>
