<h1>What's Your Style?</h1>
<p>At the Vision Center, you'll be able to select from over 700 different frames! But how can you choose the style of frame that best fits you?</p>

<p>Here are some guidelines that may help. Also, come <a href="/locations.php">visit any of our locations</a>, and our vision care professionals will be happy to assist you in your selection!</p>

<h2>Find a Frame that Matches the Shape of Your Face</h2>
<p>The shape of your face is one of the first things we consider when picking out a frame style that is most flattering for you.  The Vision Council offers these tips:
<ol>
<li>Eyewear should reflect your best feature (i.e. choose a blue frame to match blue eyes).</li>
<li>Choose a frame shape that contrasts with your face shape.</li>
<li>The frame size should be in scale with your face size.</li>
</ol>
<p>Most faces are a combination of shapes and angles, but there are seven basic face shapes to consider: round, oval, oblong, base-down triangle, base-up triangle, diamond and square.</p>
<h3>Oval</h3>
<p>An oval face is considered to be the ideal shape because of its balanced proportions. To keep the oval's natural balance, look for eyeglass frames, select frames that are as wide or wider than the broadest part of the face, and avoid low, swooping temples, which will unbalance the face.
<h3>Round</h3>
<p>A round face has curved lines with the width and length in the same proportions and no angles. To make a round face appear thinner and longer, try angular narrow eyeglass frames to lengthen the face, a clear bridge that widens the eyes, and frames that are wider than they are deep, such as a rectangular shape.</p>
<h3>Oblong</h3>
<p>An oblong face is longer than it is wide and has a long straight cheek line and sometimes a longish nose. To make an oblong face appear shorter and more balanced, try frames that have more depth than width, decorative or contrasting temples that add width to the face, or a low bridge to shorten the nose.</p>
<h3>Base-Down Triangle</h3>
<p>A base-down triangular face has a narrow forehead and widens at the cheek and chin areas. To add width and emphasize the narrow upper third of the face, try frames that are heavily accented with color and detailing on the top half or cat-eye shapes.</p>
<h3>Base-Up Triangle </h3>
<p>This face has a very wide top third and small bottom third. To minimize the width of the top of the face, try frames that are wider at the bottom, very light colors and materials and rimless frame styles (which have a light, airy effect because the lenses are simply held in place by a few screws, with no surrounding frame material).</p>

<h3>Diamond</h3>
<p>Diamond-shaped faces are narrow at the eye line and jawline, and have broad cheekbones that may be high and dramatic. This is the rarest face shape. To highlight the eyes and soften the cheekbones, try frames that have detailing or distinctive brow lines, or try rimless frames or oval and cat-eye shapes.</p>

<h3>Square </h3>
<p>A square face is characterized by a strong jaw line and a broad forehead, plus the width and length are in the same proportions. To make a square face look longer and soften the angles, try gently curved, narrow frame styles, frames that have more width than depth, and narrow ovals.</p>
<h2>Picking a Color</h2>
<p>The Vision Council's three keys to color analysis are:</p>
<ol>
<li>All people have either cool (blue-based) or warm (yellow-based) coloring.</li>
<li>Everyone looks best in his or her own color base.</li>
<li>Eyewear color should complement personal coloring.</li>
</ol>
<p>The main factors that determine the best color palette are the colors of the skin, eyes and hair.</p>
<h3>Skin</h3>
<p>Skin tone is the prime element in determining coloring. All complexions fall into one of two color bases&mdash;blue (cool) or yellow (warm). A cool complexion has blue or pink undertones, and a warm complexion has a "peaches and cream" or yellow cast. Olive skin is considered cool because it is a mixture of blue and yellow. (In the United States, cool, blue-based complexions are more common than the yellow-based warm complexions. About 60 percent of the population are "cools.")</p>
<h3>Eyes</h3>
<p>Eye colors usually are a secondary element in determining coloring because of the many variations of eye color. For example, blue eyes can range from a cool almost-violet to a pale blue-gray, which is warm. Brown eyes can vary from a light cider shade (warm) through a medium-brown to a cool almost-black.</p>
<h3>Hair</h3>
<p>Hair colors also are considered warm or cool. Strawberry blond, platinum, blue-black, white, salt-and-pepper and "dishwater" brown are cool. Warm hair colors include golden blond, flat black, brown-gold, "carrot" and "dirty" gray.</p>
 
<h2>Eyeglass Frame Colors</h2>
<p>Once you have determined if you are "warm" or "cool," then you can find the eyeglass frame colors that will suit you the best.</p>
<p>Some examples of frame colors best for warm coloring are: camel, khaki, gold, copper, peach, orange, coral, off-white, fire-engine red, warm blue and blond tortoise.</p>
<p>For cool coloring, the best eyeglass frame hues are black, rose-brown, blue-gray, plum, magenta, pink, jade, blue and demi-amber (darker) tortoise.</p>


